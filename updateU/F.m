function f = F(prob,x)
%F Cost function 
%

% ====================================================================
% e = x.on_omega - prob.data; f = 0.5*(e'*e);
% ====================================================================
e = max(0, 1 - x.on_omega .* prob.data); f = 0.5 * (e' * e);
% ====================================================================

if prob.mu>0
  f = f + prob.mu*norm(1./x.sigma)^2;
end